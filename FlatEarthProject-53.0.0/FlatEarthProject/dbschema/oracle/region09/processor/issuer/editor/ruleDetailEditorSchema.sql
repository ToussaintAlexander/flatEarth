(
   	 ID           		NUMBER(4)    	NOT NULL
  	,FIELD				NUMBER(3)    	NOT NULL
	,SUBFIELD			NUMBER(2)    	NOT NULL
	,FIRE				NUMBER(1)    	NOT NULL
  	,RULE_TYPE			NUMBER(2)    	NOT NULL
  	,RULE_GROUP			NUMBER(2)    	NOT NULL
  	,BIN_LEVEL			NUMBER(2)    	NOT NULL
