CREATE OR REPLACE TRIGGER RULE_DETAIL_EDITOR_TABLE_TRIG 
BEFORE INSERT ON RULE_DETAIL_EDITOR_TABLE 
FOR EACH ROW

BEGIN
  SELECT RULE_DETAIL_EDITOR_TABLE_SEQ.NEXTVAL
  INTO   :NEW.ID
  FROM   DUAL;
END;
