(
   	 ID           		NUMBER(4)    	NOT NULL
  	,FIELD1				NUMBER(3)    	NOT NULL
	,SUBFIELD1			NUMBER(2)    	NOT NULL
	,FORMAT1			NUMBER(2)    	NOT NULL
	,LENGTH1			NUMBER(3)    	NOT NULL
  	,ENCODING1			NUMBER(2)    	NOT NULL
