  	,SOURCE1			NUMBER(2)    	NOT NULL
  	,DIRECTION1			NUMBER(2)    	NOT NULL
  	,EXISTENCE1			NUMBER(2)    	NOT NULL
  	,CHANGE1			NUMBER(2)    	NOT NULL
