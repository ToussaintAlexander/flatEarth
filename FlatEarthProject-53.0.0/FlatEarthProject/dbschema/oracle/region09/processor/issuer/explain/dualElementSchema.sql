ALTER TABLE DUAL_ELEMENT_TABLE ADD (
  CONSTRAINT DUAL_ELEMENT_TABLE_PK PRIMARY KEY (ID));

CREATE SEQUENCE DUAL_ELEMENT_TABLE_SEQUENCE START WITH 1;

CREATE OR REPLACE TRIGGER DUAL_ELEMENT_TABLE_TRIGGER 
BEFORE INSERT ON DUAL_ELEMENT_TABLE 
FOR EACH ROW

BEGIN
  SELECT DUAL_ELEMENT_TABLE_SEQUENCE.NEXTVAL
  INTO   :NEW.ID
  FROM   DUAL;
END;
